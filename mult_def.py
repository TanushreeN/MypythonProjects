# Multiply using function
__________________________
def multiply(num1, num2):
    result = num1 * num2
    return result
    
num1 = int(input("Enter num1: "))
num2 = int(input("Enter num2: "))
print(f"The multiplication of {num1} and {num2} is:", multiply(num1, num2))

